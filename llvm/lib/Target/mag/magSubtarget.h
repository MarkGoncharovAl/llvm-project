#ifndef __LLVM_LIB_TARGET_MAG_SIMSUBTARGET_H__
#define __LLVM_LIB_TARGET_MAG_SIMSUBTARGET_H__

#include "MCTargetDesc/magInfo.h"
#include "magFrameLowering.h"
#include "magISelLowering.h"
#include "magInstrInfo.h"
#include "llvm/CodeGen/SelectionDAGTargetInfo.h"
#include "llvm/CodeGen/TargetSubtargetInfo.h"
#include <string>

#define GET_SUBTARGETINFO_HEADER
#include "magGenSubtargetInfo.inc"

namespace llvm {

class StringRef;
class TargetMachine;

class magSubtarget : public magGenSubtargetInfo {
  virtual void anchor();

  magRegisterInfo RegInfo;
  magInstrInfo InstrInfo;
  magFrameLowering FrameLowering;
  magTargetLowering TLInfo;
  SelectionDAGTargetInfo TSInfo;
  magABI::ABI TargetABI = magABI::ABI_ILP32;
  MVT XLenVT = MVT::i32;
  unsigned XLen = 32;

public:
  magSubtarget(const Triple &TT, const std::string &CPU, const std::string &FS,
                const TargetMachine &TM);

  /// Definition of function is auto generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU, StringRef TuneCPU, StringRef FS);

  const magInstrInfo *getInstrInfo() const override { return &InstrInfo; }
  const magFrameLowering *getFrameLowering() const override {
    return &FrameLowering;
  }
  const magTargetLowering *getTargetLowering() const override {
    return &TLInfo;
  }
  const magRegisterInfo *getRegisterInfo() const override { return &RegInfo; }
  const SelectionDAGTargetInfo *getSelectionDAGInfo() const override {
    return &TSInfo;
  }

  auto getTargetABI() const { return TargetABI; }
  auto getXLenVT() const { return XLenVT; }
  auto getXLen() const { return XLen; }
};

} // end namespace llvm

#endif // __LLVM_LIB_TARGET_MAG_SIMSUBTARGET_H__
